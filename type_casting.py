# String to int
str_num = "123"
int_num = int(str_num)
print("String to int:", int_num)

# Int to string
int_num = 456
str_num = str(int_num)
print("Int to string:", str_num)

# Char to int
char = '7'
int_num = int(char)
print("Char to int:", int_num)

# Int to char
int_num = 65
char = chr(int_num)
print("Int to char:", char)

# Float to int
float_num = 3.14
int_num = int(float_num)
print("Float to int:", int_num)

# Int to float
int_num = 42
float_num = float(int_num)
print("Int to float:", float_num)
